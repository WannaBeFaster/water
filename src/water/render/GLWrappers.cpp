#include "../../headers.h"

#include "../anim/Utils.h"

#include "GLWrappers.h"

//-------------------------------------------------------------------------------------------------
// VertexArray
//-------------------------------------------------------------------------------------------------
VertexArray::VertexArray()
{
    glGenVertexArrays(1, &id_);
}

//-------------------------------------------------------------------------------------------------
VertexArray::~VertexArray()
{
    glDeleteVertexArrays(1, &id_);
}

//-------------------------------------------------------------------------------------------------
// Buffer
//-------------------------------------------------------------------------------------------------
Buffer::Buffer()
{
    glGenBuffers(1, &id_);
}

//-------------------------------------------------------------------------------------------------
Buffer::~Buffer()
{
    glDeleteBuffers(1, &id_);
}
